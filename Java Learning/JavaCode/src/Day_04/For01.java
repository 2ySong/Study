package Day_04;

public class For01 {
    public static void main(String[] args){
        //打印10个“我爱王琪”
        for (int i=0;i<10;i++){
            System.out.println("我爱王琪"+i);
        }
        int i=0;
        while(i<10){
            System.out.println("我爱王琪gou"+i);
            i++;
        }
    }
}
